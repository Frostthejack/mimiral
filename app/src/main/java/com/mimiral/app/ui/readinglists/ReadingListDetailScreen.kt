@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ReadingListDetailScreen(
    listId: Int,
    onBookClick: (Int, String) -> Unit,
    onNavigateBack: () -> Unit,
    viewModel: ReadingListsViewModel = hiltViewModel()
) {
    val uiState by viewModel.uiState.collectAsState()
    val list = uiState.lists.find { it.list.id == listId }?.list
    val books = uiState.booksInExpanded

    Scaffold(
        topBar = {
            TopAppBar(
                title = {
                    Text(
                        text = list?.name ?: "Reading List",
                        maxLines = 1,
                        overflow = TextOverflow.Ellipsis
                    )
                },
                navigationIcon = {
                    IconButton(onClick = onNavigateBack) {
                        Icon(
                            imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                            contentDescription = "Back"
                        )
                    }
                }
            )
        }
    ) { padding ->
        Box(
            modifier = Modifier
                .fillMaxSize()
                .padding(padding)
        ) {
            if (uiState.isLoading) {
                CircularProgressIndicator(
                    modifier = Modifier.align(Alignment.Center)
                )
            } else if (books.isEmpty()) {
                Column(
                    modifier = Modifier
                        .fillMaxSize()
                        .padding(32.dp),
                    horizontalAlignment = Alignment.CenterHorizontally,
                    verticalArrangement = Arrangement.Center
                ) {
                    Text(
                        text = "No books in this list",
                        style = MaterialTheme.typography.bodyLarge,
                        color = MaterialTheme.colorScheme.onSurfaceVariant
                    )
                    Spacer(modifier = Modifier.height(8.dp))
                    Text(
                        text = "Add books from the library to get started",
                        style = MaterialTheme.typography.bodyMedium,
                        color = MaterialTheme.colorScheme.onSurfaceVariant.copy(alpha = 0.7f)
                    )
                    Spacer(modifier = Modifier.height(24.dp))
                    Button(onClick = onNavigateBack) {
                        Icon(
                            imageVector = Icons.Default.LibraryBooks,
                            contentDescription = null,
                            modifier = Modifier.size(18.dp)
                        )
                        Spacer(modifier = Modifier.width(8.dp))
                        Text("Go to Library")
                    }
                }
            } else {
                LazyColumn(
                    modifier = Modifier.fillMaxSize(),
                    contentPadding = PaddingValues(vertical = 8.dp)
                ) {
                    items(books, key = { it.id }) { book ->
                        BookRowInList(
                            book = book,
                            onClick = { onBookClick(book.id, book.format) },
                            onRemove = { viewModel.removeBookFromList(book.id, listId) }
                        )
                        HorizontalDivider()
                    }
                }
            }
        }
    }
}
